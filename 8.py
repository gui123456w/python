n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))

if n1 > n2:
    print("n1 é maior que n2")
elif n1 < n2:
    print("n1 é menor que n2")
else:
    print("n1 é igual a n2")

print("\nResultados:")
print("n1 * 2 =", n1 * 2)
print("n1 * 3 =", n1 * 3)
print("n1² =", n1 ** 2)

print("n2 * 2 =", n2 * 2)
print("n2² =", n2 ** 2)